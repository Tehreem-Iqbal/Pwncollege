// 0x63, 0xa8, 0x61, 0xa8, 0x57, 0x01, 0x6e, 0x79, 0xfd, 0x07, 0x3b, 0x53, 0xe2, 0x03, 0xc8, 0x1e, 0x6f, 0x2b, 0x8e, 0x09, 0xff, 0x72, 0xdf, 0x6e, 0x42, 0x4d, 0x62, 0x38
int main() {
    unsigned char array1[] = { 
    0x63, 0xa8, 0x61, 0xa8, 0x57, 0x01, 0x6e, 0x79, 0xfd, 0x07, 0x3b, 0x53, 0xe2, 0x03, 0xc8, 0x1e, 0x6f, 0x2b, 0x8e, 0x09, 0xff, 0x72, 0xdf, 0x6e, 0x42, 0x4d, 0x62, 0x38
    };

    unsigned char additions[] = {
     0xFE, 0x9B, 0x3C, 0xCC, 0xE3, 0xB0, 0x1E, 0xE6, 0x88, 0x12, 0xA1, 0xD3, 0xC3, 0x02, 0xB2, 0xDF, 0x95, 0xF6, 0x98, 0x57, 0x66, 0x0C, 0x77, 0xE5, 0x6D, 0x42, 0xBF, 0x1C
    };

    int size1 = sizeof(array1) / sizeof(array1[0]);
    int size2 = sizeof(additions) / sizeof(additions[0]);
    int min_size = size1 < size2 ? size1 : size2;

    unsigned char result[min_size];

    for (int i = 0; i < min_size; i++) {
        result[i] = array1[i] - additions[min_size - i - 1];
    }

    printf("Result: ");
    for (int i = 0; i < min_size; i++) {
        printf("0x%02X ", result[27-i]);
    }
    printf("\n");

 
    return 0;
}
